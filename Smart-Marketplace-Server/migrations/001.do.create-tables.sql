CREATE TABLE users (
id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
Name TEXT NOT NULL,
Email TEXT NOT NULL,
Location TEXT NOT NULL,
Username TEXT NOT NULL,
Password TEXT NOT NULL
);

CREATE TABLE listings (
id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
Title TEXT NOT NULL,
Owner TEXT NOT NULL,
Location TEXT NOT NULL,
Date DATE NOT NULL,
Condition TEXT NOT NULL,
Price TEXT NOT NULL,
Description TEXT NOT NULL
);